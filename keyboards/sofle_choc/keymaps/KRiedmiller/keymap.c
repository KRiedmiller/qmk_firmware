static void print_status_narrow(void) {
    oled_write_P(PSTR("Sofle"), false);
    oled_write_P(PSTR("\n\n\n"), false);
    // Print current mode
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_ln_P(PSTR("qwert"), false);
            break;
        case 4:
            oled_write_ln_P(PSTR("qwert"), false);
            break;
        case 5:
            oled_write_ln_P(PSTR("wasd\n"), false);
            break;
        default:
            oled_write_P(PSTR("Mod\n\n"), false);
            break;
    }
    oled_write_P(PSTR("\n\n"), false);
    // Print current layer
    oled_write_ln_P(PSTR("LAYER"), false);
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_P(PSTR("Base"), false);
            break;
        case 1:
            oled_write_P(PSTR("Num"), false);
            break;
        case 2:
            oled_write_P(PSTR("Arrow"), false);
            break;
        case 3:
            oled_write_P(PSTR("RGB"), false);
            break;
        case 4:
            oled_write_P(PSTR("Game1"), false);
            break;
        case 5:
            oled_write_P(PSTR("Game2"), false);
            break;
        case 6:
            oled_write_P(PSTR("GameF"), false);
            break;
        default:
            oled_write_ln_P(PSTR("Undef"), false);
    }
    oled_write_P(PSTR("\n\n"), false);
}


